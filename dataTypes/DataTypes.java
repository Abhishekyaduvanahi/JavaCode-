package dataTypes;

public class DataTypes {

	public static void main(String[] args) {
		
		boolean a = true;
		char b ='R';
		byte c = 12;
		short d = -123;
		int e = 345643;
		
		// capital l is the special suffix for long data type 
		
		long f = -432543234L;
		
		// capital f is the special suffix for float type data .
		//float is  used for large dataType for single precision .
		float g = 5.67834F;
		
		// double is used for double precision .
		
		double h = 5.6734;
      
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
		
	}

}
