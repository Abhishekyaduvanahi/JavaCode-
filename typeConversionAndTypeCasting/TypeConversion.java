/*
 
 Type conversion is done when you assign the value of smaller dataType ,
  into a bigger dataType.
   
 */

package typeConversionAndTypeCasting;

public class TypeConversion {

	public static void main(String[] args) {
		
		// implicit conversion is done automatically  so we can say its an implicit TypeCasting 
		double a;
		int b=200;
		a= b;
		System.out.println(a);
		

	}

}
